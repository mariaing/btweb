export class Empresa{
    public id?: number;
    public nit: string;
    public nombre: string;
    public telefono: string;
    public direccion: string;
    public password: string;
    public latitud: string;
    public longitud: string;

    constructor() {
    }

    public getSelectName(){
        return this.nit + ' - ' + this.nombre;
    }
}